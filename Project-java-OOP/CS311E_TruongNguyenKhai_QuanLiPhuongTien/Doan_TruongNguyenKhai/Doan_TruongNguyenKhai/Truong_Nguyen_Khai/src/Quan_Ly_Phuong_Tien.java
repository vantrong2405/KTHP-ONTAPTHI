    private void btThemMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemMoiActionPerformed
        String id, hangSanXuat = "";
        double namSanXuat, giaBan;
        String id1 = txtID.getText();
        String hang1 = txtHangSanXuat.getText();
        String namSanXuat1 = txtNamSanXuat.getText();
        String giaBan1 = txtGiaBan.getText();
        String mauXe1 = txtMauXe.getText();
        String scn1 = txtSoCho.getText();
        String ldc1 = txtLDC.getText();
        String cs1 = txtCongSuat.getText();
        String tt1 = txtLTrongTai.getText();
        String mauXe;
        id = txtID.getText();
        int index = cbbLoaiXe.getSelectedIndex();
        String lx = "";
        if (index >= 0) {
            lx = cbbLoaiXe.getItemAt(index);
        }
        String batKyTu1 = lx.substring(0, 4);
//        String batKyTu = id.substring(0, 1);
        if (id1.isEmpty()) {
//              JOptionPane.showMessageDialog(rootPane, "Mã không được để trống", "Thông báo", HEIGHT);
            JOptionPane.showMessageDialog(null, "Mã không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else if (hang1.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Hãng sản xuất không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else if (namSanXuat1.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Năm sản xuất không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else if (giaBan1.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Giá bán không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else if (mauXe1.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Màu xe không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else if (batKyTu1.trim().equalsIgnoreCase("Xe o".trim())) {
            if (scn1.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Số chỗ ngồi không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
            } else if (ldc1.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Loại động cơ không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
            } else {
                double soChoNgoi;
                String kieuDongCo = "";
                hangSanXuat = txtHangSanXuat.getText();
                namSanXuat = Double.parseDouble(txtNamSanXuat.getText());
                giaBan = Double.parseDouble(txtGiaBan.getText());
                mauXe = txtMauXe.getText();
                soChoNgoi = Double.parseDouble(txtSoCho.getText());
                kieuDongCo = txtLDC.getText();
                a.add(new Oto(soChoNgoi, kieuDongCo, id, hangSanXuat, mauXe, namSanXuat, giaBan));
            }
        } else if (batKyTu1.trim().equalsIgnoreCase("Xe m".trim())) {
            if (cs1.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Công suất không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
            } else {
                String congSuat = "";
                hangSanXuat = txtHangSanXuat.getText();
                namSanXuat = Double.parseDouble(txtNamSanXuat.getText());
                giaBan = Double.parseDouble(txtGiaBan.getText());
                mauXe = txtMauXe.getText();
                congSuat = txtCongSuat.getText();
                a.add(new XeMay(congSuat, id, hangSanXuat, mauXe, namSanXuat, giaBan));
            }
        } else if (batKyTu1.trim().equalsIgnoreCase("Xe t".trim())) {
            if (tt1.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Trọng tải không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
            } else {
                String trongTai;
                hangSanXuat = txtHangSanXuat.getText();
                namSanXuat = Double.parseDouble(txtNamSanXuat.getText());
                giaBan = Double.parseDouble(txtGiaBan.getText());
                mauXe = txtMauXe.getText();
                trongTai = txtLTrongTai.getText();
                a.add(new XeTai(trongTai, id, hangSanXuat, mauXe, namSanXuat, giaBan));
            }

        }

        showResult();
    }//GEN-LAST:event_btThemMoiActionPerformed

