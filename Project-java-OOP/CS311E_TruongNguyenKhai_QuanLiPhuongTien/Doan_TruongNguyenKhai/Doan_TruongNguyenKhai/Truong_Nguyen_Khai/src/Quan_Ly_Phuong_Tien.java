    private void btnLamMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLamMoiActionPerformed
        // TODO add your handling code here:

        int choice = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc chắn muốn làm mới danh sách ? ");
        if (JOptionPane.YES_OPTION == choice) {
            for (int i = 0; i < a.size(); i++) {
                a.remove(a.get(i));
                i--;
            }
        }
        showResult();
    }//GEN-LAST:event_btnLamMoiActionPerformed

    public void showResult() {//lay du lieu ( tra về dữ liệu ) // in ra du lieu tren bang
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
        for (PhuongTien y : a) {
            if (y instanceof Oto) {
                model.addRow(new Object[]{
                    y.getId(), y.getHangSanXuat(), (int) y.getNamSanXuat(), y.getGiaBan(), y.getMauXe(), (int) ((Oto) y).getSoChoNgoi(),
                    ((Oto) y).getKieuDongCo(), "", ""
                });
            } else if (y instanceof XeMay) {
                model.addRow(new Object[]{
                    y.getId(), y.getHangSanXuat(), (int) y.getNamSanXuat(), y.getGiaBan(), y.getMauXe(), "", "", ((XeMay) y).getCongSuat(), ""
                });
            } else if (y instanceof XeTai) {
                model.addRow(new Object[]{
                    y.getId(), y.getHangSanXuat(), (int) y.getNamSanXuat(), y.getGiaBan(), y.getMauXe(), "", "", "", ((XeTai) y).getTrongTai()
                });
            }
        }
    }

