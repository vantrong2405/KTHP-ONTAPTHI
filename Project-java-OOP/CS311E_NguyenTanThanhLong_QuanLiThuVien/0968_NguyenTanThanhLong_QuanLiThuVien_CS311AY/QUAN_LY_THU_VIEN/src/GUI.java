    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String maPM1 = txtMaPM.getText(), 
        ngayTra1 = txtNgayMuon.getText(), 
        hanTra1 = txtHanTra.getText(), 
        soHieuSach1 = txtSoHieuSach.getText(), 
        hoTen1 = txtHoTen.getText(), 
        tuoi1 = txtTuoi.getText(), lop1 = txtLop.getText();

        if (maPM1.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Mã không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else if (ngayTra1.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ngày trả không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else if (hanTra1.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Hạn trả không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else if (soHieuSach1.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Số hiệu sách không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else if (hoTen1.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Họ tên không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else if (tuoi1.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Tuổi không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else if (lop1.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Lớp không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        } else {
            String ma, soHieuSach, hoTen, lop;
            int ngayMuon, hanTra, tuoi;
            ma = txtMaPM.getText();
            ngayMuon = Integer.parseInt(txtNgayMuon.getText());
            hanTra = Integer.parseInt(txtHanTra.getText());
            soHieuSach = txtSoHieuSach.getText();
            hoTen = txtHoTen.getText();
            tuoi = Integer.parseInt(txtTuoi.getText());
            lop = txtLop.getText();
            TheMuon y = new TheMuon(ma, ngayMuon, hanTra, soHieuSach, hoTen, tuoi, lop);;
            if (x.them(y)) {
                loadDuLieu();
            } else {
                JOptionPane.showMessageDialog(null, "Mã phiếu mượn " + ma + " bị trùng", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        }


    }//GEN-LAST:event_btnThemActionPerformed

