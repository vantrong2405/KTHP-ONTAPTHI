    private void btnLamMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLamMoiActionPerformed
        // TODO add your handling code here:

        int choice = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc chắn muốn làm mới danh sách ? ");
        if (JOptionPane.YES_OPTION == choice) {
            for (int i = 0; i < qlTheMuon.size(); i++) {
                qlTheMuon.remove(qlTheMuon.get(i));
                i--;
            }
        }
        loadDuLieu();
    }//GEN-LAST:event_btnLamMoiActionPerformed

    private void btnXoa1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoa1ActionPerformed
        // TODO add your handling code here:
        String maXoa = removeID.getText();
        if (maXoa.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Mã cần xóa không được bỏ trống", "Thông báo", JOptionPane.WARNING_MESSAGE);
        }
        for (int i = 0; i <qlTheMuon.size(); i++) {
            if (qlTheMuon.get(i).getMaPhieuMuon().trim().equalsIgnoreCase(maXoa.trim())) {
                qlTheMuon.remove(qlTheMuon.get(i));
                i--;
            }
        }

       loadDuLieu();
    }//GEN-LAST:event_btnXoa1ActionPerformed

