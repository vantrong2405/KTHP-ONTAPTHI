    public void loadDuLieu() {
        qlTheMuon = x.getA();
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        for (TheMuon y : qlTheMuon) {
            if (y instanceof TheMuon) {
                dtm.addRow(new Object[]{y.getMaPhieuMuon(), y.getNgayMuon(), y.getHanTra(), y.getSoHieuSach(),
                    ((SinhVien) y).getHoTen(), ((SinhVien) y).getTuoi(), ((SinhVien) y).getLop()
                });
            }
        }
    }

